% 例2.1 设计一个感知器，将二维的四组输入矢量分成两类(解法2）
P=[-0.5 -0.5 0.3 0; -0.5 0.5 -0.5 1];  %输入矢量初始化
T=[1 1 0 0];  %目标矢量初始化
net = newp(minmax(P),1);
A = sim(net,P)      %训练前的网络输出
net.trainParam.epochs = 20;     %定义最大循环次数
net = train(net,P,T);       %训练网络，使输出和期望相同
W = net.iw{1,1}     %输出训练后的网络权值
B = net.b{1}        %输出训练后的网络偏差
A = sim(net,P)      %训练后的网络输出